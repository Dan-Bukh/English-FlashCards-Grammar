    <li>to ru<x-red>n</x-red> → ru<x-red>nn</x-red>ing</li>
    <li>to trave<x-red>l</x-red> → trave<x-red>ll</x-red>ing / trave<x-red>l</x-red>ing</li>
</ul>

<h4 class="mb-3">Отрицание</h4>
<p>Отрицание в <strong>Future Continuous</strong> образуется путем добавления частицы <strong>not</strong> после глагола <strong>will</strong> или <strong>shall</strong>.</p>
  <ol>
    <li>I <x-red>will not</x-red> be work<x-red>ing</x-red> here. – Я не буду работать здесь.</li>
    <li>He <x-red>will not</x-red> be drink<x-red>ing</x-red> tea with us. – Он не будет пить с нами чай.</li>
    <li>We <x-red>will not</x-red> be danc<x-red>ing</x-red> all night. – Мы не будем танцевать всю ночь.<br>will not = won’t (сокращение)</li>
    <li>I <x-red>won’t</x-red> be work<x-red>ing</x-red> here. – Я не буду работать здесь.<br>shall not = shan’t</li>
    <li>We <x-red>shan’t</x-red> be danc<x-red>ing</x-red> all night. – Мы не будем танцевать всю ночь.</li>
</ol>  

<h4 class="mb-3">Вопрос</h4>
<p>Образование общего вопроса в <strong>Future Continuous</strong> происходит с помощью вынесения вспомогательного глагола <strong>will</strong> (или <strong>shall</strong>) в начало предложения перед <a href="#">подлежащим</a>.</p>
<ol>
    <li><x-red>Will</x-red> I be working here? – Я буду здесь работать?</li>
    <li><x-red>Will</x-red> he be drinking tea with us? – Он будет пить с нами чай?</li>
    <li><x-red>Will</x-red> we be dancing all night? – Мы будем танцевать всю ночь?</li>
</ol>




<a href="#">Специальный вопрос</a> в <strong>Future Continuous</strong> образуется с помощью <a href="#">вопросительного слова</a> или <a href="#">фразы</a>, которая ставится в самом начале предложения. Дальнейший порядок слов такой же, как в общем вопросе для <strong>Future Continuous</strong>.
<ol>
    <li><x-red>What will</x-red> I be doing here? – Что я буду здесь делать?</li>
    <li><x-red>Whom will</x-red> he be drinking tea with? – С кем он будет пить чай?</li>
    <li><x-red>How long will</x-red> we be dancing? – Как долго мы будем танцевать?</li>
</ol>
