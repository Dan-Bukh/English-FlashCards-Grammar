<h3 class="paragraph-title" id="otritsanie">
    Отрицание  </h3>
<div class="paragraph-content ll-text">
    <p>Отрицание в <strong>Future Continuous</strong> образуется путем добавления частицы <strong>not </strong>после глагола <strong>will </strong>или <strong>shall</strong>.</p>

<ul>
  <li><em><span class="js-speech">I<span style="color:#EB4E36;"> will not be</span> working here.</span> – Я не буду работать здесь.</em></li>
  <li><em><span class="js-speech">He <span style="color:#EB4E36;">will not be</span> drinking tea with us.</span> – Он не будет пить с нами чай.</em></li>
  <li><em><span class="js-speech">We <span style="color:#EB4E36;">will not be</span> dancing all night.</span> – Мы не будем танцевать всю ночь.</em></li>
</ul>

<ul>
  <li><em><span class="js-speech">will not = won’t</span> (сокращение)</em></li>
  <li><em><span class="js-speech">I <span style="color:#EB4E36;">won’t</span> be working here.</span> – Я не буду работать здесь.</em></li>
</ul>

<ul>
  <li><em><span class="js-speech">shall not = shan’t</span></em></li>
  <li><em><span class="js-speech">We <span style="color:#EB4E36;">shan’t </span>be dancing all night.</span> – Мы не будем танцевать всю ночь.</em></li>
</ul>
</div>
</article>
</div><div class="field-item even"><article class="paragraph" about="" typeof="">
<h3 class="paragraph-title" id="vopros">
    Вопрос  </h3>
<div class="paragraph-content ll-text">
    <p>Образование общего вопроса в <strong>Future Continuous</strong> происходит с помощью вынесения вспомогательного глагола <strong>will </strong>(или <strong>shall</strong>) в начало предложения перед <a href="principal-parts-of-the-sentence#the-subject">подлежащим</a>.</p>

<ul>
  <li><em><span class="js-speech"><span style="color:#EB4E36;">Will </span>I be working here?</span> – Я буду здесь работать?</em></li>
  <li><em><span class="js-speech"><span style="color:#EB4E36;">Will </span>he be drinking tea with us?</span> – Он будет пить с нами чай?</em></li>
  <li><em><span class="js-speech"><span style="color:#EB4E36;">Will </span>we be dancing all night?</span> – Мы будем танцевать всю ночь?</em></li>
</ul>

<p><a href="types-of-questions#spetsialnye-voprosy">Специальный вопрос</a> в <strong>Future Continuous</strong> образуется с помощью <a href="question-words">вопросительного слова</a> или <a href="question-words#voprositelnye-frazy">фразы</a>, которая ставится в самом начале предложения. Дальнейший порядок слов такой же, как в общем вопросе для <strong>Future Continuous</strong>.</p>

<ul>
  <li><em><span class="js-speech"><span style="color:#EB4E36;">What will</span> I be doing here?</span> – Что я буду здесь делать?</em></li>
  <li><em><span class="js-speech"><span style="color:#EB4E36;">Whom will</span> he be drinking tea with?</span> – С кем он будет пить чай?</em></li>
  <li><em><span class="js-speech"><span style="color:#EB4E36;">How long will</span> we be dancing?</span> – Как долго мы будем танцевать?</em></li>
</ul>
</div>
</article>
</div><div class="field-item odd"><h2 class="caption-title" id="upotreblenie-future-continuous" about="" typeof="">

Употребление Future Continuous</h2>
</div><div class="field-item even"><article class="paragraph" about="" typeof="">
<h3 class="paragraph-title" id="prodolzhitelnye-deistviia-v-konkretnyi-moment-vremeni-v-budushchem">
    Продолжительные действия в конкретный момент времени в будущем  </h3>
<div class="paragraph-content ll-text">
    <p><strong>Future Continuous</strong> используется для описания действий, которые будут происходить в конкретный, точно известный момент времени или некоторый промежуток времени в будущем.</p>

<ul>
  <li><em><span class="js-speech">I will be waiting for you here <span style="color:#EB4E36;">at 9 a.m</span>. tomorrow.</span> – Я буду ждать тебя здесь завтра в девять утра.</em></li>
  <li><em><span class="js-speech">Tom will be playing football <span style="color:#EB4E36;">all day</span> next Sunday.</span> – Том будет играть в футбол всё следующее воскресенье.</em></li>
  <li><em><span class="js-speech"><span style="color:#EB4E36;">This time</span> next week I will be flying to Berlin.</span> – В это же время на следующей неделе я буду лететь в Берлин.</em></li>
</ul>

<p>Точный момент времени в <strong>Future Continuous </strong>может уточняться другим, более кратковременным действием, которое выражено в <a href="present-simple">Present Simple</a>.</p>

<ul>
  <li><em><span class="js-speech">I will be cooking dinner <span style="color:#EB4E36;">when she leaves</span>.</span> – Я буду готовить обед, когда она уйдет.</em></li>
  <li><em><span class="js-speech">He will be washing the car <span style="color:#EB4E36;">when the postman comes</span>.</span> – Он будет мыть машину, когда придет почтальон.</em></li>
  <li><em><span class="js-speech">They will be watching the game <span style="color:#EB4E36;">when the rain starts</span>.</span> – Они будут смотреть игру, когда начнется дождь.</em></li>
</ul>
</div>
</article>
</div><div class="field-item odd"><article class="paragraph" about="" typeof="">
<h3 class="paragraph-title" id="odnovremennye-deistviia-v-budushchem">
    Одновременные действия в будущем  </h3>
<div class="paragraph-content ll-text">
    <p><strong>Future Continuous</strong> используется для описания нескольких действий в одном предложении, которые будут происходить одновременно в будущем.</p>

<ul>
  <li><em><span class="js-speech">I will be making cocktails and Ann will be meeting guests.</span> – Я буду делать коктейли, а Анна будет встречать гостей.</em></li>
  <li><em><span class="js-speech">Jane will be listening to music, Jake will be reading a&nbsp;book.</span> – Джейн будет слушать музыку, а Джейк будет читать книгу.</em></li>
  <li><em><span class="js-speech">They will be sitting, watching movie and eating a pie.</span> – Они будут сидеть, смотреть фильм и есть пирог.</em></li>
</ul>
</div>
</article>
</div><div class="field-item even"><article class="paragraph" about="" typeof="">
<h3 class="paragraph-title" id="sobytiia-kotorye-nepremenno-proizoidut">
    События, которые непременно произойдут  </h3>
<div class="paragraph-content ll-text">
    <p><strong>Future Continuous</strong> используется для описания события, которое непременно произойдет в ближайшем будущем вследствии предварительной договоренности или неизбежности.</p>

<ul>
  <li><em><span class="js-speech">I saw the news. It will be raining tomorrow.</span> – Я смотрел новости: завтра будет дождь.</em></li>
  <li><em><span class="js-speech">We have&nbsp;made a deal. I shall be working instead of her next month.</span> – Мы заключили сделку. Я буду работать вместо неё в следующем месяце.</em></li>
  <li><em><span class="js-speech">We have made an arrangement. I will be meeting her soon.</span> – Мы договорились – я встречу ее скоро.</em></li>
</ul>
</div>
</article>
</div><div class="field-item odd"><article class="paragraph" about="" typeof="">
<h3 class="paragraph-title" id="zaplanirovannye-deistviia">
    Запланированные действия  </h3>
<div class="paragraph-content ll-text">
    <p><strong>Future Continuous</strong>&nbsp;может использоваться, чтобы сообщить о запланированных действиях, которые произойдут в ближайшем будущем, однако в настоящее время чаще используется <a href="present-continuous">Present Continuous</a>&nbsp;</p>

<ul>
  <li><em><span class="js-speech">She will be meeting Jake’s parents this evening.</span> – &nbsp;Она встречается с родителями Джейка сегодня вечером.</em></li>
  <li><em><span class="js-speech">She is meeting Jake’s parents this evening.</span> – Она встречается с родителями Джейка сегодня вечером.</em></li>
</ul>

<ul>
  <li><em><span class="js-speech">Lara will be flying to New York next morning.</span> – Лара летит в Нью-Йорк следующим утром.</em></li>
  <li><em><span class="js-speech">Lara is flying to New York next morning.</span> – Лара летит в Нью-Йорк следующим утром.</em></li>
</ul>

<ul>
  <li><em><span class="js-speech">I will be visiting&nbsp;the party tonight.</span> – &nbsp;Я посещу вечеринку сегодня вечером.</em></li>
  <li><em><span class="js-speech">I am visiting the party tonight.</span> – Я посещу вечеринку сегодня вечером.</em></li>
</ul>
</div>
</article>
</div><div class="field-item even"><article class="paragraph" about="" typeof="">
<h3 class="paragraph-title" id="vezhlivyi-vopros-o-planakh-na-budushchee">
    Вежливый вопрос о планах на будущее  </h3>
<div class="paragraph-content ll-text">
    <p><strong>Future Continuous</strong> может использоваться для того, чтобы задать вежливый вопрос о планах или действиях&nbsp;в ближайшем будущем.</p>

<ul>
  <li><em><span class="js-speech">Will you be using this book for long?</span> – Ты долго будешь пользоваться этой книгой?</em></li>
  <li><em><span class="js-speech">Will your sister be visiting you?</span> – Твоя сестра проведает тебя?</em></li>
  <li><em><span class="js-speech">Will she be staying here for long?</span> – Она долго будет здесь оставаться?</em></li>
</ul>
</div>
</article>
</div><div class="field-item odd"><h2 class="caption-title" id="markery-vremeni" about="" typeof="">

Маркеры времени</h2>
</div><div class="field-item even"><article class="paragraph" about="" typeof="">
<div class="paragraph-content ll-text">
    <p><strong>Future Continuous</strong> используется, если на вопрос «<strong>когда это будет происходить?</strong>» можно указать точное, конкретное время или промежуток времени в будущем.</p>

<ul>
  <li><em><span class="js-speech">at 5 o'clock</span> – в 5 часов</em></li>
  <li><em><span class="js-speech">at that moment</span> – в тот момент</em></li>
  <li><em><span class="js-speech">this time tomorrow</span> – в это время завтра</em></li>
  <li><em><span class="js-speech">the same time next week</span> – в то же самое время на следующей неделе</em></li>
</ul>

<ul>
  <li><em><span class="js-speech">meanwhile</span> – тем временем, между тем</em></li>
  <li><em><span class="js-speech">meantime</span> – тем временем, между тем</em></li>
  <li><em><span class="js-speech">in the meantime</span> – &nbsp;между тем, тем временем</em></li>
</ul>

<ul>
  <li><em><span class="js-speech">from two till three</span> – &nbsp;с двух до трех</em></li>
  <li><em><span class="js-speech">from Monday till Friday</span> – с понедельника по пятницу</em></li>
  <li><em><span class="js-speech">from 10 a.m. to 11 a.m. tomorrow</span> – с 10 до 11 утра завтра</em></li>
  <li><em><span class="js-speech">from 6 p.m. to 9 p.m. the day after tomorrow</span> – с 6 до 9 вечера послезавтра</em></li>
</ul>

<ul>
  <li><em><span class="js-speech">during this time</span> – в течение этого времени</em></li>
  <li><em><span class="js-speech">during the morning</span> – в течение утра</em></li>
  <li><em><span class="js-speech">during the day</span> – в течении дня</em></li>
  <li><em><span class="js-speech">during the evening</span> – в течении вечера</em></li>
  <li><em><span class="js-speech">during the afternoon</span> – в течении обеда</em></li>
</ul>

<ul>
  <li><em><span class="js-speech">for 2 hours</span> – на протяжении двух часов</em></li>
  <li><em><span class="js-speech">for a week</span> – за неделю, на протяжении недели</em></li>
  <li><em><span class="js-speech">for months</span> – месяцами, в течение нескольких месяцев</em></li>
</ul>

<ul>
  <li><em><span class="js-speech">all day long</span> – целый день</em></li>
  <li><em><span class="js-speech">all night</span> – всю ночь</em></li>
  <li><em><span class="js-speech">all the time</span> – все время</em></li>
</ul>

<ul>
  <li><em><span class="js-speech">the whole evening</span> – весь вечер</em></li>
  <li><em><span class="js-speech">the whole year</span> – весь год</em></li>
</ul>
</div>
