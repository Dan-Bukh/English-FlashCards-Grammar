    <h4 class="mb-4 p-2">Продолжительные действия в конкретный момент времени в будущем</h4>
<p><strong>Future Continuous</strong> используется для описания действий, которые будут происходить в конкретный, точно известный момент времени или некоторый промежуток времени в будущем.</p>

<ol>
    <li>I will be waiting for you here <x-red>at 9 a.m</x-red>. tomorrow. – Я буду ждать тебя здесь завтра в девять утра.</li>
    <li>Tom will be playing football <x-red>all day</x-red> next Sunday. – Том будет играть в футбол всё следующее воскресенье.</li>
    <li><x-red>This time</x-red> next week I will be flying to Berlin. – В это же время на следующей неделе я буду лететь в Берлин.</li>
</ol>
<p>Точный момент времени в <strong>Future Continuous</strong> может уточняться другим, более кратковременным действием, которое выражено в <a href="#">Present Simple</a>.</p>

<ol>
    <li>I will be cooking dinner <x-red>when she leaves</x-red>. – Я буду готовить обед, когда она уйдет.</li>
    <li>He will be washing the car <x-red>when the postman comes</x-red>. – Он будет мыть машину, когда придет почтальон.</li>
    <li>They will be watching the game <x-red>when the rain starts</x-red>. – Они будут смотреть игру, когда начнется дождь.</li>
</ol>

<h4 class="mb-4 p-2">Одновременные действия в будущем</h4>

<p><strong>Future Continuous</strong> используется для описания нескольких действий в одном предложении, которые будут происходить одновременно в будущем.</p>

<ol>
    <li>I will be making cocktails and Ann will be meeting guests. – Я буду делать коктейли, а Анна будет встречать гостей.</li>
    <li>Jane will be listening to music, Jake will be reading a book. – Джейн будет слушать музыку, а Джейк будет читать книгу.</li>
    <li>They will be sitting, watching movie and eating a pie. – Они будут сидеть, смотреть фильм и есть пирог.</li>
</ol>

<h4 class="mb-4 p-2">События, которые непременно произойдут</h4>

<p><strong>Future Continuous</strong> используется для описания события, которое непременно произойдет в ближайшем будущем вследствии предварительной договоренности или неизбежности.</p>

<ol>
    <li>I saw the news. It will be raining tomorrow. – Я смотрел новости: завтра будет дождь.</li>
    <li>We have made a deal. I shall be working instead of her next month. – Мы заключили сделку. Я буду работать вместо неё в следующем месяце.</li>
    <li>We have made an arrangement. I will be meeting her soon. – Мы договорились – я встречу ее скоро.</li>
</ol>

<h4 class="mb-4 p-2">Запланированные действия</h4>
<p><strong>Future Continuous</strong> может использоваться, чтобы сообщить о запланированных действиях, которые произойдут в ближайшем будущем, однако в настоящее время чаще используется <a href="#">Present Continuous</a> </p>

<ol>
    <li>She will be meeting Jake’s parents this evening. –  Она встречается с родителями Джейка сегодня вечером.</li>
    <li>She is meeting Jake’s parents this evening. – Она встречается с родителями Джейка сегодня вечером.</li>
    <li>Lara will be flying to New York next morning. – Лара летит в Нью-Йорк следующим утром.</li>
    <li>Lara is flying to New York next morning. – Лара летит в Нью-Йорк следующим утром.</li>
    <li>I will be visiting the party tonight. –  Я посещу вечеринку сегодня вечером.</li>
    <li>I am visiting the party tonight. – Я посещу вечеринку сегодня вечером.</li>
</ol>

<h4 class="mb-4 p-2">Вежливый вопрос о планах на будущее</h4>
<p><strong>Future Continuous</strong> может использоваться для того, чтобы задать вежливый вопрос о планах или действиях в ближайшем будущем.</p>

<ol>
    <li>Will you be using this book for long? – Ты долго будешь пользоваться этой книгой?</li>
    <li>Will your sister be visiting you? – Твоя сестра проведает тебя?</li>
    <li>Will she be staying here for long? – Она долго будет здесь оставаться?</li>
</ol>

