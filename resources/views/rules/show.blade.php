    <h4 class="mb-3">Утверждение</h4>
<p><strong>Future Continuous (Future Progressive)</strong> образуется с помощью <a href="#">вспомогательного глагола<a> <strong>will</strong> (реже <strong>shall</strong> для <strong>I</strong> и <strong>We</strong>) и <a href="#">формы продолжительного инфинитива</a> (<strong>be</strong> + глагол с окончанием <strong>-ing</strong>).</p>

<p><strong>Will</strong> в современном английском используется со всеми числами и лицами.</p>
<p><strong>Shall</strong> используется только с первым лицом <strong>I</strong>, <strong>We</strong>, но такое использование считается устаревшим.</p>

<p>В современном английском со всеми числами и лицами используется только <strong>will</strong> и это не является ошибкой. Но <strong>shall</strong> еще можно встретить в газетах, старых текстах или текстах при собеседовании на работу, при поступлении в учебное учреждение, художественной литературе.</p>

<ol>
   <li>I <x-red>shall be</x-red> work<x-red>ing</x-red> here. – Я буду здесь работать.</li>
    <li>He <x-red>will be</x-red> drink<x-red>ing</x-red> tea with us. – Он будет пить с нами чай.</li>
    <li>We <x-red>will be</x-red> danc<x-red>ing</x-red> all night. – Мы будем танцевать всю ночь. <br>shall = ‘ll (сокращение)</li>
    <li>I’<x-red>ll be</x-red>sitt<x-red>ing</x-red>there. – Я буду сидеть там. <br>will = ‘ll</li>
    <li>She’<x-red>ll</x-red> be send<x-red>ing</x-red> some letters. – Она будет отправлять какие–то письма.</li>
</ol>
<p>Все правила добавления <a href="#">окончания -ing</a> рассмотрены в другой статье.</p>

<ul>
    <li>to enter → enter<x-red>ing</x-red></li>
    <li>to l<x-red>ie</x-red> → l<x-red>y</x-red>ing</li>
