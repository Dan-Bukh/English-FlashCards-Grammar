<h3 class="paragraph-title" id="utverzhdenie">
    Утверждение  </h3>
<div class="paragraph-content ll-text">
    <p><strong>Future Continuous</strong>&nbsp;(<strong>Future Progressive</strong>) образуется с помощью <a href="verbs#vspomogatelnye-glagoly">вспомогательного глагола</a>&nbsp;<strong>will </strong>(реже&nbsp;<strong>shall </strong>для <strong>I</strong> и <strong>We</strong>) и формы <a href="infinitive#infinitiv-v-aktivnom-zaloge">продолжительного инфинитива</a> (<strong>be</strong> + глагол&nbsp;с окончанием -<strong>ing</strong>).</p>

<p><strong>Will </strong>в современном английском используется со всеми числами и лицами.<br>
<strong>Shall</strong>&nbsp;используется только с первым лицом <strong>I</strong>, <strong>We</strong>, но такое использование считается устаревшим.</p>

<p>В современном английском со всеми числами и лицами используется только <strong>will </strong>и это не является ошибкой. Но <strong>shall </strong>еще можно встретить в газетах, старых текстах или текстах при собеседовании на работу,&nbsp;при поступлении в учебное учреждение, художественной литературе.</p>

<ul>
  <li><em><span class="js-speech">I <span style="color:#EB4E36;">shall be</span> work<span style="color:#EB4E36;">ing</span> here.</span> – Я буду здесь работать.</em></li>
  <li><em><span class="js-speech">He <span style="color:#EB4E36;">will be</span> drink<span style="color:#EB4E36;">ing</span> tea with us.</span> – Он будет пить с нами чай.</em></li>
  <li><em><span class="js-speech">We <span style="color:#EB4E36;">will be </span>danc<span style="color:#EB4E36;">ing</span> all night.</span> – Мы будем танцевать всю ночь.</em></li>
</ul>

<ul>
  <li><em><span class="js-speech">shall</span> = ‘ll (сокращение)</em></li>
  <li><em><span class="js-speech">I’<span style="color:#EB4E36;">ll be</span> sitt<span style="color:#EB4E36;">ing</span> there.</span> – Я буду сидеть там.</em></li>
</ul>

<ul>
  <li><em><span class="js-speech">will</span> = ‘ll</em></li>
  <li><em><span class="js-speech">She<span style="color:#EB4E36;">’ll be</span> send<span style="color:#EB4E36;">ing</span> some letters.</span> – Она будет отправлять какие–то письма.</em></li>
</ul>

<p>Все правила добавления <a href="ing">окончания -ing</a> рассмотрены в другой статье.</p>

